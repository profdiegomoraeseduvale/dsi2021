    private void btnAcessarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAcessarActionPerformed
        
        String email = txtEmail.getText();
        String senha = new String(txtSenha.getPassword());
        
        sql = "SELECT * FROM Usuario WHERE usu_email = ? AND usu_senha = md5(?)";
        
        try {
            
            con = new Conexao();
            banco = con.conectarBanco();
            pst = banco.prepareStatement(sql);
            
            pst.setString(1, email);
            pst.setString(2, senha);
            
            rs = pst.executeQuery();
            
            if(rs.next()){
                
                JFPrincipal prin = new JFPrincipal();
                prin.setVisible(true);
                this.dispose();
                
            }else{
                JOptionPane.showMessageDialog(this, "Usu√°rio e/ou senha incorretos");
                txtEmail.setText("");
                txtSenha.setText("");
                txtEmail.requestFocus();
            }
            
            
        } catch (Exception e) {
            System.out.println("Erro: "+e);
        }
        
    }//GEN-LAST:event_btnAcessarActionPerformed

